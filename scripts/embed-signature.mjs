import sharp from "sharp";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

// Turns Leon's scanned signature (dark ink on a near-white background) into a
// clean transparent PNG, then writes a base64 module the PDF can embed — the
// same pipeline used for the crest. Run with: node scripts/embed-signature.mjs

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const src = path.join(root, "public", "media", "signatures", "leon-signature-raw.png");
const transparentOut = path.join(root, "public", "media", "signatures", "leon-signature.png");
const moduleOut = path.join(root, "src", "lib", "signature.ts");

const lum = (r, g, b) => 0.299 * r + 0.587 * g + 0.114 * b;
const clamp01 = (x) => (x < 0 ? 0 : x > 1 ? 1 : x);

// Ink colour for the rendered signature (charcoal blue-black, like a pen).
const INK = [0x22, 0x22, 0x2a];
// Luminance references: anything near white is background; mid-grey is solid ink.
const BG_L = 250;
const INK_L = 150;
const SPAN = BG_L - INK_L;

const { data, info } = await sharp(src)
  .ensureAlpha()
  .raw()
  .toBuffer({ resolveWithObject: true });
const { width, height, channels } = info;

const out = Buffer.alloc(width * height * 4);
for (let i = 0, o = 0; i < data.length; i += channels, o += 4) {
  const r = data[i];
  const g = data[i + 1];
  const b = data[i + 2];
  const srcAlpha = channels === 4 ? data[i + 3] / 255 : 1;
  const L = lum(r, g, b);
  // Alpha grows as the pixel darkens below the background luminance.
  let a = clamp01((BG_L - L) / SPAN);
  // Gentle contrast curve to keep strokes crisp without dropping faint tails.
  a = clamp01((a - 0.04) / 0.92) * srcAlpha;
  out[o] = INK[0];
  out[o + 1] = INK[1];
  out[o + 2] = INK[2];
  out[o + 3] = Math.round(a * 255);
}

await sharp(out, { raw: { width, height, channels: 4 } })
  .png()
  .trim({ threshold: 4 })
  .resize({ width: 600, fit: "inside", withoutEnlargement: true })
  .png({ compressionLevel: 9 })
  .toFile(transparentOut);

const buf = await sharp(transparentOut).png({ compressionLevel: 9 }).toBuffer();
const meta = await sharp(buf).metadata();
const b64 = buf.toString("base64");

const file = `// AUTO-GENERATED by scripts/embed-signature.mjs — do not edit by hand.
// Transparent signature (Leon See Chan) embedded into the Letter of Intent PDF.
export const SIGNATURE_PNG_WIDTH = ${meta.width};
export const SIGNATURE_PNG_HEIGHT = ${meta.height};
export const SIGNATURE_PNG_BASE64 =
  "${b64}";
`;

writeFileSync(moduleOut, file);
console.log(`Wrote ${transparentOut} (${meta.width}x${meta.height})`);
console.log(`Wrote ${moduleOut} (${(b64.length / 1024).toFixed(1)} KB base64)`);
